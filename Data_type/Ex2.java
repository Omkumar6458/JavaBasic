package Data_type;

public class Ex2 {
  public static void main(String[] args) {
        byte a = 10;
        short b = 200;
        int c = a + b;
        long d = c * 1000L;

        System.out.println("Byte: " + a);
        System.out.println("Short: " + b);
        System.out.println("Int (Sum): " + c);
        System.out.println("Long (Multiplied): " + d);
}
}
